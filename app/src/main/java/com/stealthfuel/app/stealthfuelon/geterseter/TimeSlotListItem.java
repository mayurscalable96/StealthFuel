package com.stealthfuel.app.stealthfuelon.geterseter;

public class TimeSlotListItem {

    private String TimeSlot;
    private String ExtraAmount;
    private String TimeSlotAmount;
    private String Day;
    private String CutOffTime;

    public String getCutOffTime() {
        return CutOffTime;
    }

    public void setCutOffTime(String cutOffTime) {
        CutOffTime = cutOffTime;
    }

    public String getTimeSlot() {
        return TimeSlot;
    }

    public void setTimeSlot(String timeSlot) {
        TimeSlot = timeSlot;
    }

    public String getExtraAmount() {
        return ExtraAmount;
    }

    public void setExtraAmount(String extraAmount) {
        ExtraAmount = extraAmount;
    }

    public String getTimeSlotAmount() {
        return TimeSlotAmount;
    }

    public void setTimeSlotAmount(String timeSlotAmount) {
        TimeSlotAmount = timeSlotAmount;
    }

    public String getDay() {
        return Day;
    }

    public void setDay(String day) {
        Day = day;
    }


}
